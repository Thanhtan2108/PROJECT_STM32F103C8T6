void DHT11_Start() {
  // Cau hinh PA2 thanh OUTPUT
  setToOutput();
  // gui tin hieu bat dau
  // ban dau o muc 1
  HAL_GPIO_WritePin(GPIOA, GPIO_PIN_2, 1);
  // keo xuong muc 0 va delay it nhat 18ms
  HAL_GPIO_WritePin(GPIOA, GPIO_PIN_2, 0);
  HAL_Delay(20);
  // sau do set lai len 1
  HAL_GPIO_WritePin(GPIOA, GPIO_PIN_2, 1);
  // chuyen PA2 dang INPUT
  setToInput();
  // cho phan hoi tu DHT
  // co the bo sung them ham thuat toan check loi

  while(HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_2) == 1); // thoat while tu co phan hoi PA2 = 0 (wait 20-40us)
  while(HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_2) == 0); // doi PA2 = 1 (wait 80us)
  while(HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_2) == 1); // doi PA2 = 0 (wait 80us)
  // No da xuong 0 -> 40 bits da bat dau duoc truyen
  // ket thuc gui tin hieu bat dau
}

uint8_t DHT11_ReadByte() {

  uint8_t result = 0, i = 0;

  // doc 1 byte = 8 bit
  for(i = 0 ; i < 8 ; i++) {
    while(HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_2) == 0);
    Delay_us(50);
    if(HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_2) == 1) {
      // bit 1
      result = (result << 1) | (1 << 0); 
    } else {
      // bit 0
      result = (result << 1) & ~(1 << 0);
    }
    while(HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_2) == 1);
  }

  return result;
}

